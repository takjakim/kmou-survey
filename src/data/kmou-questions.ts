export function getQuestionsByPart(part: 'A' | 'B' | 'C'): SurveyQuestion[] {
  return KMOU_SURVEY_QUESTIONS.filter((q) => q.part === part);
}

export function getQuestionsBySection(sectionId: string): SurveyQuestion[] {
  return KMOU_SURVEY_QUESTIONS.filter((q) => q.section === sectionId);
}

export function getTotalQuestionCount(): number {
  return KMOU_SURVEY_QUESTIONS.length;
}

export function getRequiredQuestionCount(): number {
  return KMOU_SURVEY_QUESTIONS.filter((q) => q.required).length;
}
